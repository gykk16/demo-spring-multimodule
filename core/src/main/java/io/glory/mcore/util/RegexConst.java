package io.glory.mcore.util;

/**
 * 정규식
 */
public class RegexConst {

    /**
     * 숫자만 허용
     */
    public static final String ALLOW_ONLY_NUMBER_REGEX = "[^\\d]";

    // ================================================================================================================
    // Replace 용 정규식
    // ================================================================================================================
    /**
     * 영문 alphabet 만 허용
     */
    public static final String ALLOW_ONLY_ALPHABET_REGEX = "[^A-Za-z]";

    /**
     * 영문 alphabet, 숫자, underscore {_} 허용
     */
    public static final String ALLOW_ONLY_ALPHANUMERIC_REGEX = "[^\\w]";

    /**
     * 한글 포함 여부
     */
    public static final String CONTAINS_KOREAN_REGEX = ".*[ㄱ-ㅎㅏ-ㅣ가-힣]+.*";

    // ================================================================================================================
    // 한글, 숫자 정규식
    // ================================================================================================================
    /**
     * 숫자만 포함 여부
     */
    public static final String CONTAINS_ONLY_NUMBER_REGEX = "^[0-9]+$";

    /**
     * 핸드폰 번호 정규식
     * <ul>
     * <li>{3} {3,4} {4} </li>
     * <li>010, 011, 016 ~ 019 로 시작 </li>
     * <li>구분자 {.}, {-}, {구분자 없음} 허용 </li>
     * </ul>
     * <ul>
     * <li>01011112222 = true </li>
     * <li>010.1111.2222 = true </li>
     * <li>010-1111-2222 = true </li>
     * </ul>
     */
    public static final String MOBILE_NO_REGEX = "^01(?:0|1|[6-9])[.-]?(\\d{3}|\\d{4})[.-]?(\\d{4})$";

    // ================================================================================================================
    // 전화 번호 정규식
    // ================================================================================================================
    /**
     * 일반 전호 번호 정규식
     * <ul>
     * <li>일반 지역 전화 : 02, 031, 032, 051 ... 등 </li>
     * <li>인터넷 전화 : 070 </li>
     * <li>구분자 {.}, {-}, {구분자 없음} 허용 </li>
     * <li>! 080, 090, 0505 ... 등 허용 안함 </li>
     * </yl>
     */

    public static final String PHONE_NO_REGEX = "^0(2|70|[3-6][1-5])[.-]?(\\d{3}|\\d{4})[.-]?(\\d{4})$";

    /**
     * 대표 번호 정규식
     * <p>
     * 1500-1234
     * </p>
     */
    public static final String OFFICIAL_PHONE_NO_REGEX = "^(\\d{4})[.-]?(\\d{4})$";

    // ================================================================================================================
    // 비밀번호 정규식
    // ================================================================================================================
    /**
     * 비밀번호 정규식
     * <ul>
     * <li>영문, 특수문자, 숫자 포함 11자 이상 </li>
     * <li>비밀번호는 영문과 특수문자 숫자를 포함하며 11자 이상이어야 한다. </li>
     * </ul>
     */
    public static final String PWD_REGEX = "^(?=.*[a-zA-Z])(?=.*\\d)(?=.*\\W).{11,30}$";

    private RegexConst() {
    }

    // ================================================================================================================
    //
    // ================================================================================================================

}
